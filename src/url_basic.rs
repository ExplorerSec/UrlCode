use std::collections::HashMap;

pub fn url_encode(s:&str)->String{
    let map: HashMap<&str, &str> = HashMap::from([
        (" ", "%20"),
        ("!", "%21"),
        ("\"", "%22"),
        ("'", "%27"),
        ("(", "%28"),
        (")", "%29"),
        ("*", "%2A"),
        ("+", "%2B"),
        (",", "%2C"),
        ("/", "%2F"),
        (":", "%3A"),
        (";", "%3B"),
        ("<", "%3C"),
        (">", "%3E"),
        ("@", "%40"),
        ("[", "%5B"),
        ("]", "%5D"),
        ("\\", "%5C"),
        ("_", "%5F"),
        ("^", "%5E"),
        ("`", "%60"),
        ("{", "%7B"),
        ("|", "%7C"),
        ("}", "%7D"),
        //("~", "%7E"),
        ]);
        let mut out = String::from(s);
        for i in map{
            out = out.replace(i.0, i.1);
        }
        out

}

